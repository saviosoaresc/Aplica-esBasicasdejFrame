    private void converterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_converterActionPerformed
        double res = 0;
        
        switch (jComboBox.getSelectedIndex()) {
            case 1:
                res = Double.valueOf(inicial.getText()) * 4.91;
                break;
            case 2:
                res = Double.valueOf(inicial.getText()) * 5.40;
                break;
            case 3:
                res = Double.valueOf(inicial.getText()) * 0.037;
                break;
            default:
                break;
        }
        DecimalFormat df = new DecimalFormat("#.##");
        resultado.setText(String.valueOf(df.format(res)));
           
    }//GEN-LAST:event_converterActionPerformed

